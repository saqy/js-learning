// Array Push method, Array.push(); Adding Element at the end of an Array.
var number_arr = [10, 20, 30, 40];
var names_arr = ["Sadaqat", "Usman", "Imshaz", "Salaman"];

console.log("Original array    ", number_arr);
// apply push method
number_arr.push(50, 60, 70);

console.log("After applying push method    ", number_arr);


console.log("Original array    ", names_arr);
// apply push method
names_arr.push("Ritu");

console.log("After applying push method    ", names_arr);
