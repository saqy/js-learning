
// Array.pop() : Removing elements from the end of an array===
var number_arr = [10, 20, 30, 40, 50];
var names_arr = ["Sadaqat", "Usman", "Kaleem", "Imshaz", "Salaman"];

console.log("Original array    ", number_arr);
// applying pop method
number_arr.pop();

console.log("After applying pop method    ", number_arr);

console.log("Original array    ", names_arr);
// applying pop method
names_arr.pop();

console.log("After applying pop method    ", names_arr);